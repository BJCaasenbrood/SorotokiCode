function Y = isomse3(X)

if numel(X) == 6
    S = isom(X(1:3));
    U = X(4:6);
    
    Y = zeros(4,4);
    Y(1:3,1:3) = S;
    Y(1:3,4)   = U;
    
elseif numel(X) == 16
    S = X(1:3,1:3);
    U = X(1:3,4);
    
    Y = zeros(6,1);
    Y(1:3) = isom_(S);
    Y(4:6) = U;
end

end

