function Dist = PneuNet(P)
  R1 = dRectangle(P,0,10,0,10);
  R2 = dRectangle(P,-4,0.5,2,12);
  R3 = dRectangle(P,9.5,12,2,12);
  C1 = dCircle(P,0,2.5,0.5);
  C2 = dCircle(P,10,2.5,0.5);
  Dist = dDiff(dDiff(dDiff(dDiff(R1,R2),R3),C1),C2);
end

